def password():
  import random
  n=int(input("enter the length of the password"))
  list=[]
  for i in range(0,n):
    a=random.randint(0,9)
    list.append(a)
  #print(list)
  random.shuffle(list)
  password=""
  for i in list:
    password=password+str(i)
  print(f"the password which is generated by using numbers is{password}")
password()
