  print("welcome to password genarator")
  n=int(input("enter how many numbers do you want in your password"))
  m=int(input("enter how many symbols do you want in your password"))
  o=int(input("enter how many alphabets do you want in your password"))
  password=[]
  for i in range(n):
    p=random.choice(numbers)
    password.append(p)
  for i in range(m):
    q=random.choice(symbols)
    password.append(q)
  for i in range(o):
    r=random.choice(alphabets)
